package org.crce.interns.beans;


public class PlacedStudentsBean {

	private String year;
	
	
	private int it;
	
	
	private int comps;
	
	
	private int elex;
	
	
	private int prod;

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public int getIt() {
		return it;
	}

	public void setIt(int it) {
		this.it = it;
	}

	public int getComps() {
		return comps;
	}

	public void setComps(int comps) {
		this.comps = comps;
	}

	public int getElex() {
		return elex;
	}

	public void setElex(int elex) {
		this.elex = elex;
	}

	public int getProd() {
		return prod;
	}

	public void setProd(int prod) {
		this.prod = prod;
	}

}
